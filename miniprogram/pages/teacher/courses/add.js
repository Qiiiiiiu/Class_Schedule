  async onSaveChildCourse() {
    const { editCourseId, editDate, editStartTime, editEndTime, editClassroom, editIndex } = this.data

    if (!editCourseId) {
      wx.showToast({ title: '课程ID不存在', icon: 'none' })
      return
    }

    if (!editDate) {
      wx.showToast({ title: '请选择上课日期', icon: 'none' })
      return
    }

    const [startHour, startMin] = editStartTime.split(':').map(Number)
    const [endHour, endMin] = editEndTime.split(':').map(Number)
    if (endHour * 60 + endMin <= startHour * 60 + startMin) {
      wx.showToast({ title: '结束时间必须晚于开始时间', icon: 'none' })
      return
    }

    wx.showLoading({ title: '保存中...' })

    const isoDate = editDate.replace(/\//g, '-')

    try {
      const result = await api.editCourseSchedule(editCourseId, {
        date: isoDate,
        startTime: editStartTime,
        endTime: editEndTime,
        classroom: editClassroom
      })

      wx.hideLoading()

      if (result) {
        wx.showToast({ title: '修改成功', icon: 'success' })
        this.setData({ showEditModal: false })

        const generatedCourses = [...this.data.generatedCourses]
        generatedCourses[editIndex] = {
          ...generatedCourses[editIndex],
          schedule: {
            ...generatedCourses[editIndex].schedule,
            date: isoDate,
            startTime: editStartTime,
            endTime: editEndTime,
            classroom: editClassroom
          },
          scheduleStr: this.formatScheduleStr(isoDate, editStartTime, editEndTime)
        }
        this.setData({ generatedCourses })
        // 更新日历视图
        this.updateCalendarView(generatedCourses)
      }
    } catch (err) {
      wx.hideLoading()
      console.error('保存课程失败:', err)
      wx.showToast({ title: '修改失败', icon: 'none' })
    }
  },

