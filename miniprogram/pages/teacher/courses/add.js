const app = getApp()
const checkRole = require('../../../utils/checkRole.js')
const api = require('../../../utils/api.js')

Page({
  data: {
    mode: 'add',
    courseId: '',
    parentCourseId: '',
    parentCourseCreated: false,
    isChildOnlyMode: false,
    lockedParentInfo: false,
    courseName: '',
    price: '',
    courseDate: '',
    startTime: '09:00',
    endTime: '10:00',
    classroom: '',
    reminderIndex: 0,
    reminderOptions: [
      { value: 0, label: '不提醒' },
      { value: 1, label: '提前1分钟' },
      { value: 5, label: '提前5分钟' },
      { value: 10, label: '提前10分钟' },
      { value: 15, label: '提前15分钟' },
      { value: 30, label: '提前30分钟' }
    ],
    status: 'available',
    loading: false,
    students: [],
    selectedStudents: [],
    selectedStudentNames: '',
    courseNameAutoGenerated: false,
    showGeneratedCourses: false,
    generatedCourses: [],
    isRepeat: false,
    weekdayOptions: [
      { value: 0, label: '周日', selected: false },
      { value: 1, label: '周一', selected: false },
      { value: 2, label: '周二', selected: false },
      { value: 3, label: '周三', selected: false },
      { value: 4, label: '周四', selected: false },
      { value: 5, label: '周五', selected: false },
      { value: 6, label: '周六', selected: false }
    ],
    selectedWeekdays: [],
    repeatCount: 1,
    parentCourses: [],
    bindToExisting: false,
    selectedParentCourseId: '',
    selectedParentCourseIndex: -1,
    bindToExistingForRepeat: false,
    selectedParentCourseIdForRepeat: '',
    selectedParentCourseIndexForRepeat: -1
  },

  onLoad(options) {
    if (!checkRole.checkTeacher()) {
      return
    }

    const { mode, courseId, courseData } = options
    this.setData({
      mode: mode || 'add',
      courseId: courseId || ''
    })

    const now = new Date()
    const defaultDate = `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}`
    this.setData({ courseDate: defaultDate })

    if (mode === 'edit' && courseData) {
      try {
        const decodedData = decodeURIComponent(courseData)
        const course = JSON.parse(decodedData)

        this.setData({
          courseName: course.name,
          price: '',
          courseDate: defaultDate,
          startTime: course.schedule ? course.schedule.startTime : '09:00',
          endTime: course.schedule ? course.schedule.endTime : '10:00',
          classroom: course.schedule ? course.schedule.classroom : '',
          reminderIndex: this.getReminderIndex(course.reminderTime || 0),
          status: course.status,
          selectedStudents: course.students || [],
          parentCourseId: course._id,
          lockedParentInfo: true,
          isChildOnlyMode: true
        })
      } catch (err) {
        console.error('Failed to parse course data:', err)
      }
    }

    this.loadStudents()
    this.loadParentCourses()

    if (mode === 'edit' && courseId) {
      this.loadChildCourses(courseId)
    }
  },

  async loadParentCourses() {
    const teacherId = app.globalData.openid
    const res = await api.getCourses(teacherId)
    if (res && res.length > 0) {
      this.setData({
        parentCourses: res.map(course => ({
          _id: course._id,
          name: course.name,
          scheduleStr: `${course.schedule.startTime}-${course.schedule.endTime}`
        }))
      })
    }
  },

  onBindToExistingChange(e) {
    if (this.data.lockedParentInfo) {
      wx.showToast({
        title: '父课程已锁定，无法切换',
        icon: 'none'
      })
      return
    }
    this.setData({
      bindToExisting: e.detail.value,
      selectedParentCourseId: '',
      selectedParentCourseIndex: -1
    })
  },

  onBindToExistingForRepeatChange(e) {
    if (this.data.lockedParentInfo) {
      wx.showToast({
        title: '父课程已锁定，无法切换',
        icon: 'none'
      })
      return
    }
    this.setData({
      bindToExistingForRepeat: e.detail.value,
      selectedParentCourseIdForRepeat: '',
      selectedParentCourseIndexForRepeat: -1
    })
  },

  onParentCourseChange(e) {
    const index = parseInt(e.detail.value)
    if (!isNaN(index) && index >= 0 && this.data.parentCourses[index]) {
      const parentCourse = this.data.parentCourses[index]
      const parentStudents = parentCourse.students || []

      let updatedStudents = [...this.data.students]
      updatedStudents = updatedStudents.map(student => ({
        ...student,
        selected: parentStudents.includes(student.studentId)
      }))

      const selectedNames = updatedStudents.filter(s => s.selected).map(s => s.studentName).join('、')

      this.setData({
        selectedParentCourseIndex: index,
        selectedParentCourseId: parentCourse._id,
        courseName: parentCourse.name,
        courseNameAutoGenerated: false,
        students: updatedStudents,
        selectedStudentNames: selectedNames
      })
    } else {
      this.setData({
        selectedParentCourseIndex: -1,
        selectedParentCourseId: ''
      })
    }
  },

  onParentCourseForRepeatChange(e) {
    const index = parseInt(e.detail.value)
    if (!isNaN(index) && index >= 0 && this.data.parentCourses[index]) {
      this.setData({
        selectedParentCourseIndexForRepeat: index,
        selectedParentCourseIdForRepeat: this.data.parentCourses[index]._id
      })
    } else {
      this.setData({
        selectedParentCourseIndexForRepeat: -1,
        selectedParentCourseIdForRepeat: ''
      })
    }
  },

  async loadChildCourses(parentId) {
    const res = await api.getCourseSchedules(parentId)
    if (res && res.length > 0) {
      const childCourses = res.map(course => ({
        ...course,
        scheduleStr: `${course.schedule.date} ${course.schedule.startTime}-${course.schedule.endTime}`
      }))
      this.setData({
        generatedCourses: childCourses,
        showGeneratedCourses: true
      })
    }
  },

  getReminderIndex(value) {
    const options = this.data.reminderOptions
    for (let i = 0; i < options.length; i++) {
      if (options[i].value === value) return i
    }
    return 0
  },

  async loadStudents() {
    const teacherId = app.globalData.openid
    const res = await api.getStudents(teacherId)

    if (res) {
      const selectedStudents = this.data.selectedStudents || []

      const students = res.map(item => ({
        studentId: item.studentId,
        studentName: item.studentName,
        phone: item.studentPhone,
        status: item.status,
        selected: selectedStudents.includes(item.studentId)
      }))

      const selectedNames = students.filter(s => s.selected).map(s => s.studentName).join('、')

      this.setData({
        students,
        selectedStudentNames: selectedNames
      })
    }
  },

  onStudentSelect(e) {
    if (this.data.lockedParentInfo) {
      wx.showToast({
        title: '父课程已锁定，无法修改学生',
        icon: 'none'
      })
      return
    }

    const studentId = e.currentTarget.dataset.studentId
    const students = [...this.data.students]
    const student = students.find(item => item.studentId === studentId)

    if (student) {
      student.selected = !student.selected

      const selectedStudents = students.filter(item => item.selected)
      const selectedNames = selectedStudents.map(s => s.studentName).join('、')

      if (selectedStudents.length === 1 && !this.data.courseName) {
        this.setData({
          courseName: `${selectedStudents[0].studentName}的课程`,
          courseNameAutoGenerated: true,
          students,
          selectedStudentNames: selectedNames
        })
      } else if (selectedStudents.length !== 1 && this.data.courseNameAutoGenerated) {
        this.setData({
          courseName: '',
          courseNameAutoGenerated: false,
          students,
          selectedStudentNames: selectedNames
        })
      } else {
        this.setData({
          students,
          selectedStudentNames: selectedNames
        })
      }
    }
  },

  onCourseNameInput(e) {
    if (this.data.lockedParentInfo) {
      wx.showToast({
        title: '父课程已锁定，无法修改课程名称',
        icon: 'none'
      })
      return
    }
    this.setData({
      courseName: e.detail.value,
      courseNameAutoGenerated: false
    })
  },

  onPriceInput(e) {
    if (this.data.lockedParentInfo) {
      wx.showToast({
        title: '父课程已锁定，无法修改价格',
        icon: 'none'
      })
      return
    }
    this.setData({ price: e.detail.value })
  },

  onDateChange(e) {
    this.setData({ courseDate: e.detail.value })
  },

  onStartTimeChange(e) {
    const startTime = e.detail.value
    this.setData({ startTime })
    
    const [startHour, startMinute] = startTime.split(':').map(Number)
    let endHour = startHour + 1
    let endMinute = startMinute
    
    if (endHour >= 24) {
      endHour = 23
      endMinute = 59
    }
    
    const endTime = `${String(endHour).padStart(2, '0')}:${String(endMinute).padStart(2, '0')}`
    this.setData({ endTime })
  },

  onEndTimeChange(e) {
    this.setData({ endTime: e.detail.value })
  },

  isValidTimeRange(startTime, endTime) {
    const [startHour, startMinute] = startTime.split(':').map(Number)
    const [endHour, endMinute] = endTime.split(':').map(Number)
    
    const startTotalMinutes = startHour * 60 + startMinute
    const endTotalMinutes = endHour * 60 + endMinute
    
    return endTotalMinutes > startTotalMinutes
  },

  onClassroomInput(e) {
    this.setData({ classroom: e.detail.value })
  },

  onReminderChange(e) {
    const newIndex = e.detail.value
    const oldIndex = this.data.reminderIndex

    // 如果从"不提醒"改为其他选项，需要确认订阅消息
    if (oldIndex === 0 && newIndex !== 0) {
      wx.showModal({
        title: '订阅课程提醒',
        content: '开启课程提醒需要订阅消息服务，课程开始前将通过微信消息提醒您。是否确认订阅？',
        confirmText: '确认订阅',
        cancelText: '暂不订阅',
        success: (res) => {
          if (res.confirm) {
            this.requestSubscribe(() => {
              this.setData({ reminderIndex: newIndex })
            }, () => {
              // 订阅失败，保持不提醒状态
              this.setData({ reminderIndex: 0 })
              wx.showToast({
                title: '订阅失败，已设为不提醒',
                icon: 'none'
              })
            })
          } else {
            // 用户取消，保持不提醒状态
            this.setData({ reminderIndex: 0 })
          }
        },
        fail: () => {
          this.setData({ reminderIndex: 0 })
        }
      })
    } else {
      this.setData({ reminderIndex: newIndex })
    }
  },

  async requestSubscribe(successCallback, failCallback) {
    try {
      const res = await wx.requestSubscribeMessage({
        tmplIds: ['w223WKtyfXebjCpkNc2TbtczBuGuR4SUk2IRbMcRreU']
      })

      if (res['w223WKtyfXebjCpkNc2TbtczBuGuR4SUk2IRbMcRreU'] === 'accept') {
        wx.showToast({
          title: '订阅成功',
          icon: 'success'
        })
        successCallback && successCallback()
      } else {
        failCallback && failCallback()
      }
    } catch (err) {
      console.error('订阅失败:', err)
      failCallback && failCallback()
    }
  },

  onStatusChange(e) {
    this.setData({ status: e.detail.value ? 'available' : 'unavailable' })
  },

  onRepeatChange(e) {
    this.setData({
      isRepeat: e.detail.value,
      selectedWeekdays: e.detail.value ? [] : this.data.selectedWeekdays,
      repeatCount: e.detail.value ? 1 : this.data.repeatCount
    })
  },

  onWeekdayTap(e) {
    const value = parseInt(e.currentTarget.dataset.value)
    if (isNaN(value)) {
      return
    }
    const selectedWeekdays = [...(this.data.selectedWeekdays || [])]
    const index = selectedWeekdays.indexOf(value)

    if (index === -1) {
      selectedWeekdays.push(value)
    } else {
      selectedWeekdays.splice(index, 1)
    }

    selectedWeekdays.sort((a, b) => a - b)

    const weekdayOptions = this.data.weekdayOptions.map(item => ({
      ...item,
      selected: selectedWeekdays.indexOf(item.value) !== -1
    }))

    this.setData({
      selectedWeekdays,
      weekdayOptions,
      repeatCount: selectedWeekdays.length
    })
  },

  onRepeatCountInput(e) {
    const value = parseInt(e.detail.value) || 1
    this.setData({ repeatCount: Math.max(1, value) })
  },

  async onSave() {
    const { courseName, price, courseDate, startTime, endTime, classroom, reminderIndex, status, mode, courseId, parentCourseId, students, isRepeat, selectedWeekdays, repeatCount, bindToExisting, selectedParentCourseId, bindToExistingForRepeat, selectedParentCourseIdForRepeat, lockedParentInfo } = this.data

    if (!courseName) {
      wx.showToast({
        title: '请输入课程名称',
        icon: 'none'
      })
      return
    }

    if (!isRepeat && !courseDate) {
      wx.showToast({
        title: '请选择上课日期',
        icon: 'none'
      })
      return
    }

    if (isRepeat && (!selectedWeekdays || selectedWeekdays.length === 0)) {
      wx.showToast({
        title: '请选择重复的星期',
        icon: 'none'
      })
      return
    }

    if (!startTime || !endTime) {
      wx.showToast({
        title: '请选择上课时间',
        icon: 'none'
      })
      return
    }

    if (!this.isValidTimeRange(startTime, endTime)) {
      wx.showToast({
        title: '结束时间必须大于开始时间',
        icon: 'none'
      })
      return
    }

    if (bindToExisting && !selectedParentCourseId) {
      wx.showToast({
        title: '请选择要绑定的课程',
        icon: 'none'
      })
      return
    }

    this.setData({ loading: true })

    const selectedStudents = students.filter(item => item.selected).map(item => item.studentId)

    try {
      let result

      if (mode === 'edit') {
        const courseData = {
          name: courseName,
          teacherId: app.globalData.openid,
          teacherName: app.globalData.userInfo.name,
          schedule: {
            date: null,
            startTime,
            endTime,
            classroom
          },
          reminderTime: this.data.reminderOptions[reminderIndex].value,
          status,
          students: selectedStudents
        }
        result = await api.editCourse(courseId, courseData)
      } else if (isRepeat && bindToExisting && selectedParentCourseId) {
        const scheduleData = {
          name: courseName,
          price: parseFloat(price) || 0,
          teacherId: app.globalData.openid,
          teacherName: app.globalData.userInfo.name,
          parentId: selectedParentCourseId,
          schedule: {
            date: null,
            startTime,
            endTime,
            classroom
          },
          reminderTime: this.data.reminderOptions[reminderIndex].value,
          status,
          students: selectedStudents,
          isRepeat: true,
          weekdays: selectedWeekdays,
          repeatCount
        }
        result = await api.addCourseSchedule(scheduleData)
      } else if (isRepeat && !parentCourseId && !bindToExisting) {
        const courseData = {
          name: courseName,
          price: parseFloat(price) || 0,
          teacherId: app.globalData.openid,
          teacherName: app.globalData.userInfo.name,
          schedule: {
            date: null,
            startTime,
            endTime,
            classroom
          },
          reminderTime: this.data.reminderOptions[reminderIndex].value,
          status,
          students: selectedStudents,
          isRepeat: true,
          weekdays: selectedWeekdays,
          repeatCount
        }
        result = await api.addCourse(courseData)
      } else if (isRepeat && parentCourseId) {
        const scheduleData = {
          name: courseName,
          price: parseFloat(price) || 0,
          teacherId: app.globalData.openid,
          teacherName: app.globalData.userInfo.name,
          parentId: parentCourseId,
          schedule: {
            date: null,
            startTime,
            endTime,
            classroom
          },
          reminderTime: this.data.reminderOptions[reminderIndex].value,
          status,
          students: selectedStudents,
          isRepeat: true,
          weekdays: selectedWeekdays,
          repeatCount
        }
        result = await api.addCourseSchedule(scheduleData)
      } else if (bindToExisting && selectedParentCourseId) {
        const scheduleData = {
          name: courseName,
          price: parseFloat(price) || 0,
          teacherId: app.globalData.openid,
          teacherName: app.globalData.userInfo.name,
          parentId: selectedParentCourseId,
          schedule: {
            date: courseDate,
            startTime,
            endTime,
            classroom
          },
          reminderTime: this.data.reminderOptions[reminderIndex].value,
          status,
          students: selectedStudents,
          isRepeat: false
        }
        result = await api.addCourseSchedule(scheduleData)
      } else if (parentCourseId) {
        const scheduleData = {
          name: courseName,
          price: parseFloat(price) || 0,
          teacherId: app.globalData.openid,
          teacherName: app.globalData.userInfo.name,
          parentId: parentCourseId,
          schedule: {
            date: courseDate,
            startTime,
            endTime,
            classroom
          },
          reminderTime: this.data.reminderOptions[reminderIndex].value,
          status,
          students: selectedStudents,
          isRepeat: false
        }
        result = await api.addCourseSchedule(scheduleData)
      } else {
        const courseData = {
          name: courseName,
          price: parseFloat(price) || 0,
          teacherId: app.globalData.openid,
          teacherName: app.globalData.userInfo.name,
          schedule: {
            date: courseDate,
            startTime,
            endTime,
            classroom
          },
          reminderTime: this.data.reminderOptions[reminderIndex].value,
          status,
          students: selectedStudents,
          isRepeat: false
        }
        result = await api.addCourse(courseData)
      }

      if (result) {
        if (mode === 'edit') {
          wx.showToast({
            title: '修改成功',
            icon: 'success'
          })
          setTimeout(() => {
            wx.navigateBack()
          }, 1500)
        } else {
          if (result.courses && result.courses.length > 0) {
            const generatedCourses = result.courses.map(course => ({
              ...course,
              scheduleStr: `${course.schedule.date} ${course.schedule.startTime}-${course.schedule.endTime}`
            }))

            this.setData({
              generatedCourses: [...this.data.generatedCourses, ...generatedCourses],
              showGeneratedCourses: true,
              parentCourseCreated: true,
              lockedParentInfo: true,
              parentCourseId: result.parentId || parentCourseId
            })

            wx.showToast({
              title: '添加成功',
              icon: 'success'
            })
          } else if (result._id) {
            const newCourse = {
              ...result,
              scheduleStr: `${result.schedule.date} ${result.schedule.startTime}-${result.schedule.endTime}`
            }
            this.setData({
              generatedCourses: [...this.data.generatedCourses, newCourse],
              showGeneratedCourses: true,
              parentCourseCreated: true,
              lockedParentInfo: true,
              parentCourseId: result.parentId || parentCourseId
            })

            wx.showToast({
              title: '添加成功',
              icon: 'success'
            })
          } else {
            wx.showToast({
              title: '添加成功',
              icon: 'success'
            })
          }
        }
      }
    } catch (err) {
      console.error('Save course error:', err)
    } finally {
      this.setData({ loading: false })
    }
  },

  onEditChildCourse(e) {
    const index = parseInt(e.currentTarget.dataset.index)
    const course = this.data.generatedCourses[index]

    if (!course) {
      wx.showToast({
        title: '课程信息不存在',
        icon: 'none'
      })
      return
    }

    wx.showModal({
      title: '修改子课程',
      editable: true,
      placeholderText: `日期: ${course.schedule.date}\n时间: ${course.schedule.startTime}-${course.schedule.endTime}`,
      success: async (res) => {
        if (res.confirm && res.content) {
          try {
            const parts = res.content.split('\n')
            let date = course.schedule.date
            let startTime = course.schedule.startTime
            let endTime = course.schedule.endTime

            for (const part of parts) {
              if (part.includes('日期')) {
                date = part.replace(/日期:\s*/, '').trim()
              } else if (part.includes('时间')) {
                const timeRange = part.replace(/时间:\s*/, '').trim()
                const [start, end] = timeRange.split('-')
                startTime = start.trim()
                endTime = end.trim()
              }
            }

            const result = await api.editCourseSchedule(course._id, {
              schedule: {
                ...course.schedule,
                date,
                startTime,
                endTime
              }
            })

            if (result) {
              wx.showToast({
                title: '修改成功',
                icon: 'success'
              })
              const updatedCourse = {
                ...course,
                schedule: {
                  ...course.schedule,
                  date,
                  startTime,
                  endTime
                },
                scheduleStr: `${date} ${startTime}-${endTime}`
              }
              const generatedCourses = [...this.data.generatedCourses]
              generatedCourses[index] = updatedCourse
              this.setData({ generatedCourses })
            }
          } catch (err) {
            console.error('Edit child course error:', err)
            wx.showToast({
              title: '修改失败',
              icon: 'none'
            })
          }
        }
      }
    })
  },

  onDeleteChildCourse(e) {
    const id = e.currentTarget.dataset.id

    wx.showModal({
      title: '确认删除',
      content: '确定要删除这节课程吗？',
      success: async (res) => {
        if (res.confirm) {
          try {
            const result = await api.deleteCourseSchedule(id)
            if (result) {
              wx.showToast({
                title: '删除成功',
                icon: 'success'
              })
              const generatedCourses = this.data.generatedCourses.filter(item => item._id !== id)
              this.setData({ generatedCourses })
            }
          } catch (err) {
            console.error('Delete child course error:', err)
            wx.showToast({
              title: '删除失败',
              icon: 'none'
            })
          }
        }
      }
    })
  }
})